"""Analyze and plot trading-policy results produced by run_train_test.py.

Each run writes a pickle to results/<model_name>/results_*.pickle containing a
dict keyed by model_tag, with per-run fields: returns, sharpe, ret, std,
turnover, short_proportion, config, timestamp.

Usage:
    python3 tools/analyze_results.py                 # scan all of results/
    python3 tools/analyze_results.py -m CNNTransformer
    python3 tools/analyze_results.py -d results/CNNTransformer
"""
import argparse
import glob
import os
import pickle

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

# Trading days per year, used to annualize the (daily) Sharpe ratio.
TRADING_DAYS = 252


def load_runs(results_dir):
    """Load every run from every pickle under results_dir into one flat dict."""
    runs = {}
    pattern = os.path.join(results_dir, "**", "*.pickle")
    for path in sorted(glob.glob(pattern, recursive=True)):
        try:
            with open(path, "rb") as f:
                results_dict = pickle.load(f)
        except Exception as e:  # noqa: BLE001 - keep scanning other files
            print(f"  ! skipping {path}: {e!r}")
            continue
        for tag, data in results_dict.items():
            data = dict(data)
            data["_source_file"] = path
            runs[tag] = data
    return runs


def _scalar(v):
    """Reduce a possibly per-period array (e.g. turnover) to a scalar mean."""
    if isinstance(v, (list, np.ndarray)):
        a = np.asarray(v, dtype=float)
        return float(np.nanmean(a)) if a.size else np.nan
    return v if v is not None else np.nan


def _parse_tag(tag):
    """Pull the factor model and experiment variant out of a model_tag."""
    factor = tag.split("__", 1)[0]
    if "gnn-extension" in tag:
        variant = "CNN+Transformer+GNN"
    elif "paper-baseline" in tag:
        variant = "CNN+Transformer"
    else:
        variant = "other"
    return factor, variant


def summarize(runs):
    """Build a tidy DataFrame of headline metrics, one row per run."""
    rows = []
    for tag, d in runs.items():
        rets = np.asarray(d.get("returns", []), dtype=float)
        daily_sharpe = d.get("sharpe", np.nan)
        factor, variant = _parse_tag(tag)
        rows.append({
            "factor_model": factor,
            "variant": variant,
            "sharpe_daily": daily_sharpe,
            "sharpe_annual": daily_sharpe * np.sqrt(TRADING_DAYS)
                             if daily_sharpe == daily_sharpe else np.nan,
            "mean_ret": _scalar(d.get("ret")),
            "std": _scalar(d.get("std")),
            "turnover": _scalar(d.get("turnover")),
            "short_proportion": _scalar(d.get("short_proportion")),
            "n_days": rets.size,
            "model_tag": tag,
            "timestamp": d.get("timestamp"),
        })
    df = pd.DataFrame(rows)
    if not df.empty:
        df = df.sort_values("sharpe_annual", ascending=False).reset_index(drop=True)
    return df


def write_latex_table(df, outpath):
    """Write the summary as a LaTeX tabular fragment for \\input{} in the report."""
    num_cols = ["sharpe_annual", "mean_ret", "std", "turnover", "short_proportion"]
    headers = ["Factor model", "Architecture", "Sharpe (ann.)", "Mean ret.",
               "Std.", "Turnover", "Short prop."]

    def esc(s):
        return str(s).replace("_", r"\_").replace("&", r"\&").replace("%", r"\%")

    lines = [
        r"% Auto-generated by tools/analyze_results.py -- do not edit by hand.",
        r"\begin{tabular}{l l r r r r r}",
        r"\toprule",
        " & ".join(headers) + r" \\",
        r"\midrule",
    ]
    for _, row in df.iterrows():
        cells = [esc(row["factor_model"]), esc(row["variant"])]
        for c in num_cols:
            v = row[c]
            cells.append(f"{v:.3f}" if v == v else "--")  # NaN-safe
        lines.append(" & ".join(cells) + r" \\")
    lines += [r"\bottomrule", r"\end{tabular}"]

    with open(outpath, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  saved LaTeX table -> {outpath}")


def plot_equity_curves(runs, outpath, top_n=None):
    """Plot cumulative returns (equity curves) for runs, best Sharpe first."""
    ordered = sorted(
        runs.items(),
        key=lambda kv: kv[1].get("sharpe", float("-inf")),
        reverse=True,
    )
    if top_n:
        ordered = ordered[:top_n]

    plt.figure(figsize=(12, 7))
    plotted = 0
    for tag, d in ordered:
        rets = np.asarray(d.get("returns", []), dtype=float)
        if rets.size == 0:
            continue
        equity = np.cumprod(1.0 + rets)
        factor, variant = _parse_tag(tag)
        sr_ann = d.get("sharpe", float("nan")) * np.sqrt(TRADING_DAYS)
        plt.plot(equity, label=f"{factor} | {variant} (SR={sr_ann:.2f})")
        plotted += 1

    if plotted == 0:
        print("  ! no runs with return series to plot")
        plt.close()
        return

    plt.yscale("log")
    plt.xlabel("Trading day (out-of-sample)")
    plt.ylabel("Cumulative return (log scale, start = 1.0)")
    plt.title("Equity curves by trading policy")
    plt.legend(fontsize=8, loc="upper left")
    plt.grid(True, which="both", alpha=0.3)
    plt.tight_layout()
    plt.savefig(outpath, dpi=150)
    plt.close()
    print(f"  saved equity-curve plot -> {outpath}")


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("-d", "--dir", default=None,
                        help="results directory to scan (default: results/ or results/<model>)")
    parser.add_argument("-m", "--model", default=None,
                        help="model name subfolder under results/ (e.g. CNNTransformer)")
    parser.add_argument("-n", "--top-n", type=int, default=None,
                        help="only plot the top-N runs by Sharpe")
    parser.add_argument("-o", "--out", default="results/summary.csv",
                        help="path to write the summary CSV")
    args = parser.parse_args()

    results_dir = args.dir or (os.path.join("results", args.model) if args.model else "results")
    if not os.path.isdir(results_dir):
        raise SystemExit(f"Directory not found: {results_dir}")

    print(f"Scanning {results_dir} ...")
    runs = load_runs(results_dir)
    if not runs:
        raise SystemExit("No runs found. Are the .pickle files downloaded yet?")
    print(f"Loaded {len(runs)} run(s).")

    df = summarize(runs)
    pd.set_option("display.width", 200)
    print("\n=== Summary (sorted by annualized Sharpe) ===")
    show_cols = ["factor_model", "variant", "sharpe_daily", "sharpe_annual",
                 "mean_ret", "std", "turnover", "short_proportion", "n_days"]
    print(df[show_cols].to_string(index=False))

    os.makedirs(os.path.dirname(args.out) or ".", exist_ok=True)
    df.to_csv(args.out, index=False)
    print(f"\nWrote summary -> {args.out}")

    outroot = os.path.dirname(args.out) or "."
    write_latex_table(df, os.path.join(outroot, "summary_table.tex"))
    plot_path = os.path.join(outroot, "equity_curves.png")
    plot_equity_curves(runs, plot_path, top_n=args.top_n)


if __name__ == "__main__":
    main()
